# Funções
# Declarando uma função -> Aqui só estamos declarando a função
# Para que ela seja operada é necessário chama-la
def lin():
    print('-=' * 10)

# Chamando a função
lin()
print(f"{'    APRENDA PYTHON   '}")
lin()

# Inserindo parâmetros nas funções
def titulo(msg):
    print(msg)

lin()
titulo('    APRENDA PYTHON     ')
lin()
titulo('    CURSO EM VIDEO     ')